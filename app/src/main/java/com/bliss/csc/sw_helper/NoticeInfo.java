package com.bliss.csc.sw_helper;

public class NoticeInfo {
    private String title;
    private String url;


    public void setNoTitle(String title) {
        this.title = title;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getNoTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}
